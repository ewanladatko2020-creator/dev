# 1. Получаем сумму и преобразуем в дробное число
rub_amount = float(input('Введите сумму в рублях: '))
# Пользователь вводит "1000" → становится числом 1000.0

# 2. Получаем код валюты (оставляем строкой)
currency = input('В какую валюту перевести? (USD, EUR, CNY): ')
# Пользователь вводит "USD" → остается строкой "USD"

# 3. Проверяем, какую валюту выбрали
if currency == 'USD':        # Если ввели "USD"
    rate = 90.5              # Устанавливаем курс для доллара
elif currency == 'EUR':      # Иначе если ввели "EUR"
    rate = 98.3              # Устанавливаем курс для евро
elif currency == 'CNY':      # Иначе если ввели "CNY"
    rate = 12.7              # Устанавливаем курс для юаня
else:                        # Во всех остальных случаях
    print('Нет такой валюты...')  # Сообщаем об ошибке
    rate = 0                 # Ставим курс 0 (чтобы избежать деления на 0)

# 4. Если курс установлен правильно (не 0)
if rate > 0:
    # Делим рубли на курс: 1000 RUB / 90.5 = 11.05 USD
    converter_amount = rub_amount / rate
    
    # Выводим результат с форматированием
    print(f'{rub_amount} RUB = {converter_amount:.2f} {currency}')